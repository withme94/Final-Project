package kr.co.vo;

public class CourseVO {
	private int cno;
	private String title;
	private String content;
	private String writer;
	private String tema;
	private String schedule;
	private String listsee;
	private String mainsee;
	
	public int getCno() {
		return cno;
	}
	public void setCno(int cno) {
		this.cno = cno;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getTema() {
		return tema;
	}
	public void setTema(String tema) {
		this.tema = tema;
	}
	public String getSchedule() {
		return schedule;
	}
	public void setSchedule(String schedule) {
		this.schedule = schedule;
	}
	public String getListsee() {
		return listsee;
	}
	public void setListsee(String listsee) {
		this.listsee = listsee;
	}
	public String getMainsee() {
		return mainsee;
	}
	public void setMainsee(String mainsee) {
		this.mainsee = mainsee;
	}
	
	
}
